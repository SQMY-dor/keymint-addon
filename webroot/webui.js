    document.getElementById('btnRefreshStatus').addEventListener('click', checkDaemonStatus);
    document.getElementById('btnRestartKeymint').addEventListener('click', restartKeymint);
    document.getElementById('btnRestartInjector').addEventListener('click', restartInjector);
