async function listDir(path) {
    const nameR = await exec(`cd "${path}" 2>/dev/null && ls -1 2>/dev/null`, {});
    if (nameR.errno !== 0 || !nameR.stdout.trim()) return [];
    const names = nameR.stdout.trim().split('\n').filter(n => n && n !== '.' && n !== '..');
    const typeR = await exec(`cd "${path}" 2>/dev/null && ls -1F 2>/dev/null`, {});
    const typeLines = typeR.stdout.trim().split('\n').filter(Boolean);
    const typeMap = {};
    for (const line of typeLines) {
        if (line.endsWith('/')) typeMap[line.slice(0, -1)] = 'd';
        else if (line.endsWith('*')) typeMap[line.slice(0, -1)] = 'f';
        else typeMap[line] = 'f';
    }
    const entries = [];
    for (const name of names.slice(0, 500)) {
        const isDir = typeMap[name] === 'd';
        const escaped = `"${path}/${name}"`;
        let size = 0;
        if (!isDir) {
            const sizeR = await exec(`stat -c%s ${escaped} 2>/dev/null || wc -c < ${escaped} 2>/dev/null`, {});
            size = parseInt(sizeR.stdout.trim()) || 0;
        }
        entries.push({ name, isDir, size, fullPath: `${path}/${name}` });
    }
    return entries;
}

