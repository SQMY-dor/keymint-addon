async function loadInjectorConfig() {
    const content = await readFile(INJECTOR_PATH);
    if (!content) {
        log(__('log.read_failed'), 'error');
        currentScoop = ['io.github.vvb2060.keyattestation', 'com.google.android.gsf', 'com.google.android.gms', 'com.android.vending', 'com.eltavine.duckdetector'];
        currentDeny = [];
        return;
    }
    const config = parseInjectorToml(content);
    currentScoop = config.scoop;
    currentDeny = config.filter.deny_packages || [];
    log(__('log.read_count', {scoop: currentScoop.length, deny: currentDeny.length}));
}

function renderPkgList(filter = '') {
    const listEl = document.getElementById('pkgList');
    const numEl = document.getElementById('scoopNum');
    const denyNumEl = document.getElementById('denyNum');
    let items = currentScoop.map(pkg => ({ name: pkg, denied: currentDeny.includes(pkg) }));
    if (filter) { const f = filter.toLowerCase(); items = items.filter(i => i.name.toLowerCase().includes(f)); }
    if (items.length === 0) { listEl.innerHTML = '<div class="loading-spinner">' + __('scope.no_match') + '</div>'; numEl.textContent = currentScoop.length; denyNumEl.textContent = currentDeny.length; return; }
    listEl.innerHTML = items.map(item => `
        <div class="pkg-item">
            <span class="pkg-name">${item.denied ? '<span class="pkg-deny-badge">' + __('scope.deny_badge') + '</span>' : ''}${item.name}</span>
            <div class="pkg-actions">
                ${item.denied ? `<button class="btn-icon-small" data-action="unban" data-pkg="${item.name}" title="` + __('scope.unban_title') + `">✅</button>` : `<button class="btn-icon-small" data-action="ban" data-pkg="${item.name}" title="` + __('scope.ban_title') + `">⛔</button>`}
                <button class="btn-icon-small remove" data-action="remove" data-pkg="${item.name}" title="` + __('scope.remove_title') + `">🗑️</button>
            </div>
        </div>
    `).join('');
    numEl.textContent = currentScoop.length;
    denyNumEl.textContent = currentDeny.length;
    listEl.querySelectorAll('[data-action]').forEach(btn => {
        btn.addEventListener('click', async (e) => {
            const action = btn.dataset.action;
            const pkg = btn.dataset.pkg;
            if (action === 'remove') {
                currentScoop = currentScoop.filter(p => p !== pkg);
                currentDeny = currentDeny.filter(p => p !== pkg);
                renderPkgList(document.getElementById('pkgSearch').value);
                updateStats();
                showToast(__('toast.pkg_removed', {pkg: pkg}));
                await syncToFile();
            } else if (action === 'ban') {
                if (!currentDeny.includes(pkg)) { currentDeny.push(pkg); renderPkgList(document.getElementById('pkgSearch').value); updateStats(); showToast(__('toast.pkg_denied', {pkg: pkg})); await syncToFile(); }
            } else if (action === 'unban') {
                currentDeny = currentDeny.filter(p => p !== pkg);
                renderPkgList(document.getElementById('pkgSearch').value);
                updateStats();
                showToast(__('toast.pkg_undenied', {pkg: pkg}));
                await syncToFile();
            }
        });
    });
}

