async function checkDaemonStatus() {
    const keymintPid = await sh(`cat ${STATE_DIR}/keymint-daemon.pid 2>/dev/null`);
    const injectorPid = await sh(`cat ${STATE_DIR}/injector-daemon.pid 2>/dev/null`);
    const keymintRunning = keymintPid.stdout.trim() ? (await sh(`kill -0 ${keymintPid.stdout.trim()} 2>/dev/null && echo yes || echo no`)).stdout.trim() === 'yes' : false;
    const injectorRunning = injectorPid.stdout.trim() ? (await sh(`kill -0 ${injectorPid.stdout.trim()} 2>/dev/null && echo yes || echo no`)).stdout.trim() === 'yes' : false;
    const keymintEl = document.getElementById('keymintDaemonStatus');
    const injectorEl = document.getElementById('injectorDaemonStatus');
    keymintEl.textContent = keymintRunning ? __('controls.running') : __('controls.stopped');
    keymintEl.className = 'daemon-state ' + (keymintRunning ? 'running' : 'stopped');
    injectorEl.textContent = injectorRunning ? __('controls.running') : __('controls.stopped');
    injectorEl.className = 'daemon-state ' + (injectorRunning ? 'running' : 'stopped');
}

